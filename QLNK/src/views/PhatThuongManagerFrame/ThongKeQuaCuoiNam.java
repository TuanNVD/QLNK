    private void btnchitietActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnchitietActionPerformed
        // TODO add your handling code here:
        Connection conn = null;
        Statement stm = null;

        try {
            conn = MysqlConnection.getMysqlConnection();
            if (txtTimkiem.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Ban chua nhap gia tri tim kiem");
            } else {
                String sql1 = "select DISTINCT t.idHoKhau, dh.IDNhanKhau, dh.hoTen, pt.tenQua, pt.giaTri " +
                                "from thanh_vien_cua_ho t, nhan_khau n, phat_thuong_cuoi_nam p, phan_qua pt, danh_hieu dh " +
                                "where p.IDHS = dh.ID and p.IDPhanQua = pt.ID and dh.IDNhanKhau = n.ID "
                                + "and idHoKhau = " + txtTimkiem.getText() + "";

                PreparedStatement pstmt = conn.prepareStatement(sql1);

                ResultSet rs1 = pstmt.executeQuery();

                tblModel1.setRowCount(0);
                while (rs1.next()) {
                    tblModel1.addRow(new Object[]{
                        tblModel1.getRowCount() + 1,
                        rs1.getInt("idHoKhau"),
                        rs1.getInt("IDNhanKhau"),
                        rs1.getString("hoTen"),
                        rs1.getString("tenQua"),
                        rs1.getInt("giaTri"),});
                }

                tblModel1.fireTableDataChanged();
            }
        } catch (HeadlessException | ClassNotFoundException | SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnchitietActionPerformed

