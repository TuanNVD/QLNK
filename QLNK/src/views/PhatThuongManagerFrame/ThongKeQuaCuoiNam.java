    private void showHK() {
        Connection conn = null;
        Statement stm = null;

        try {
            conn = MysqlConnection.getMysqlConnection();

            String sql = "select idHoKhau, count(IDPhanQua) as sophanthuong, count(IDPhanQua)*10000 as sotienqua "
                    + "from nhan_khau n, thanh_vien_cua_ho t, phat_thuong_cuoi_nam p "
                    + "where t.idNhanKhau = n.id and n.ID = p.IDHS  group by idHoKhau";
            PreparedStatement pstmt = conn.prepareStatement(sql);
            ResultSet rs = pstmt.executeQuery();
            tblModel.setRowCount(0);
            while (rs.next()) {
                tblModel.addRow(new Object[]{
                    tblModel.getRowCount() + 1,
                    rs.getString("idHoKhau"),
                    rs.getInt("sophanthuong"),
                    rs.getInt("sotienqua"),});
            }
            tblModel.fireTableDataChanged();

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

